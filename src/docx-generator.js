var fs = require("fs");
var path = require("path");
var PizZip = require("pizzip");
var Docxtemplater = require("docxtemplater");

var TEMPLATE_PATH = path.join(__dirname, "../templates/Sumula_Template_v3.docx");

function formatDate(d) {
  if (!d) return "";
  if (d.indexOf("/") >= 0) return d;
  var parts = d.split("-");
  if (parts.length === 3) return parts[2] + "/" + parts[1] + "/" + parts[0];
  return d;
}

function esc(s) {
  if (!s) return "";
  return s.replace(/&/g, "&amp;").replace(/</g, "&lt;").replace(/>/g, "&gt;").replace(/"/g, "&quot;");
}

// ── Bullet XML builders ──
// Match EXACTLY the formatting from the reference model (56ª Reunião)

// Main bullet: filled circle (numId=1, ilvl=0)
function mkBullet(text) {
  return '<w:p><w:pPr>' +
    '<w:pStyle w:val="PargrafodaLista"/>' +
    '<w:numPr><w:ilvl w:val="0"/><w:numId w:val="1"/></w:numPr>' +
    '<w:ind w:right="457"/>' +
    '<w:jc w:val="both"/>' +
    '<w:rPr><w:rFonts w:asciiTheme="minorHAnsi" w:hAnsiTheme="minorHAnsi" w:cstheme="minorHAnsi"/><w:sz w:val="22"/><w:szCs w:val="22"/></w:rPr>' +
    '</w:pPr>' +
    '<w:r><w:rPr><w:rFonts w:asciiTheme="minorHAnsi" w:hAnsiTheme="minorHAnsi" w:cstheme="minorHAnsi"/><w:sz w:val="22"/><w:szCs w:val="22"/></w:rPr>' +
    '<w:t xml:space="preserve">' + esc(text) + '</w:t></w:r></w:p>';
}

// Empty spacer paragraph between bullets (matches model's PargrafodaLista empty para)
function mkBulletSpacer() {
  return '<w:p><w:pPr>' +
    '<w:pStyle w:val="PargrafodaLista"/>' +
    '<w:ind w:right="457"/>' +
    '<w:jc w:val="both"/>' +
    '<w:rPr><w:rFonts w:asciiTheme="minorHAnsi" w:hAnsiTheme="minorHAnsi" w:cstheme="minorHAnsi"/><w:sz w:val="22"/><w:szCs w:val="22"/></w:rPr>' +
    '</w:pPr></w:p>';
}

// Sub-bullet: open circle (numId=15, ilvl=1)
function mkSubBullet(text) {
  return '<w:p><w:pPr>' +
    '<w:pStyle w:val="PargrafodaLista"/>' +
    '<w:numPr><w:ilvl w:val="1"/><w:numId w:val="15"/></w:numPr>' +
    '<w:spacing w:after="120" w:line="276" w:lineRule="auto"/>' +
    '<w:ind w:right="318" w:hanging="357"/>' +
    '<w:contextualSpacing w:val="0"/>' +
    '<w:jc w:val="both"/>' +
    '<w:rPr><w:rFonts w:asciiTheme="minorHAnsi" w:hAnsiTheme="minorHAnsi" w:cstheme="minorHAnsi"/><w:sz w:val="22"/><w:szCs w:val="22"/></w:rPr>' +
    '</w:pPr>' +
    '<w:r><w:rPr><w:rFonts w:asciiTheme="minorHAnsi" w:hAnsiTheme="minorHAnsi" w:cstheme="minorHAnsi"/><w:sz w:val="22"/><w:szCs w:val="22"/></w:rPr>' +
    '<w:t xml:space="preserve">' + esc(text) + '</w:t></w:r></w:p>';
}

/**
 * Convert text content to Word XML bullet paragraphs.
 * Adds empty spacer paragraphs between main bullets (matching reference model).
 */
function contentToXml(text) {
  if (!text) return "";
  var lines = text.split("\n");
  var out = "";
  var prevWasBullet = false;
  for (var i = 0; i < lines.length; i++) {
    var line = lines[i].trim();
    if (!line) continue;
    if (line.indexOf(">> ") === 0) {
      out += mkSubBullet(line.substring(3).trim());
      prevWasBullet = false;
    } else {
      // Add spacer between main bullets (not before the first one)
      if (prevWasBullet) {
        out += mkBulletSpacer();
      }
      if (line.indexOf("- ") === 0) {
        out += mkBullet(line.substring(2).trim());
      } else {
        out += mkBullet(line);
      }
      prevWasBullet = true;
    }
  }
  return out;
}

/**
 * Post-process: find all <w:p> with paraId="FIX00004" and replace with bullets.
 * These paragraphs are generated by docxtemplater from {conteudo_secao}.
 */
function postProcess(xml, secoes) {
  if (!secoes || secoes.length === 0) {
    console.log("[postProcess] No secoes to process");
    return xml;
  }

  var MARKER = 'paraId="FIX00004"';
  
  // Find all positions
  var positions = [];
  var searchPos = 0;
  while (true) {
    var idx = xml.indexOf(MARKER, searchPos);
    if (idx < 0) break;
    positions.push(idx);
    searchPos = idx + MARKER.length;
  }

  console.log("[postProcess] Found " + positions.length + " FIX00004 paragraphs for " + secoes.length + " secoes");

  if (positions.length === 0) return xml;

  // Process in REVERSE order
  for (var i = positions.length - 1; i >= 0; i--) {
    if (i >= secoes.length) {
      console.log("[postProcess] Skipping position " + i + " — no matching secao");
      continue;
    }
    
    var conteudo = secoes[i].conteudo_secao || "";
    if (!conteudo) {
      console.log("[postProcess] Secao " + i + " has no content, skipping");
      continue;
    }

    var markerPos = positions[i];
    
    // Find the <w:p that contains this marker
    var pStart = xml.lastIndexOf("<w:p ", markerPos);
    if (pStart < 0) {
      console.log("[postProcess] Could not find <w:p for position " + i);
      continue;
    }

    // Find closing </w:p>
    var pEnd = xml.indexOf("</w:p>", markerPos);
    if (pEnd < 0) {
      console.log("[postProcess] Could not find </w:p> for position " + i);
      continue;
    }
    pEnd += 6;

    // Generate bullet XML
    var bulletXml = contentToXml(conteudo);
    if (!bulletXml) {
      console.log("[postProcess] contentToXml returned empty for secao " + i);
      continue;
    }

    console.log("[postProcess] Replacing secao " + i + ": " + (pEnd - pStart) + " chars -> " + bulletXml.length + " chars of bullets");

    // Replace
    xml = xml.substring(0, pStart) + bulletXml + xml.substring(pEnd);
  }

  return xml;
}

/**
 * Remove ANEXO section when empty.
 */
function removeEmptyAnexo(xml) {
  var anexoPos = xml.indexOf(">ANEXO<");
  if (anexoPos < 0) return xml;

  var afterAnexo = xml.substring(anexoPos);
  if (afterAnexo.indexOf("w:drawing") >= 0 || afterAnexo.indexOf("pic:pic") >= 0) {
    return xml;
  }

  // Find the empty spacer paragraph before ANEXO (sz=48)
  var pAnexo = xml.lastIndexOf("<w:p ", anexoPos);
  if (pAnexo < 0) return xml;

  // Check for preceding empty sz=48 paragraph
  var beforeAnexo = xml.substring(0, pAnexo).trimEnd();
  var prevPEnd = beforeAnexo.lastIndexOf("</w:p>");
  if (prevPEnd >= 0) {
    prevPEnd += 6;
    var checkArea = xml.substring(Math.max(0, prevPEnd - 300), prevPEnd);
    if (checkArea.indexOf('w:sz w:val="48"') >= 0) {
      var prevPStart = xml.lastIndexOf("<w:p ", prevPEnd - 300);
      if (prevPStart >= 0 && prevPStart < prevPEnd) {
        pAnexo = prevPStart;
      }
    }
  }

  var sectPr = xml.indexOf("<w:sectPr", anexoPos);
  if (sectPr < 0) return xml;

  console.log("[removeEmptyAnexo] Removing ANEXO block");
  xml = xml.substring(0, pAnexo) + xml.substring(sectPr);
  return xml;
}

async function generateSumula(data) {
  if (!fs.existsSync(TEMPLATE_PATH)) {
    throw new Error("Template nao encontrado em: " + TEMPLATE_PATH);
  }

  console.log("[generateSumula] Starting generation...");

  var content = fs.readFileSync(TEMPLATE_PATH, "binary");
  var zip = new PizZip(content);
  var doc = new Docxtemplater(zip, {
    paragraphLoop: true,
    linebreaks: true,
    delimiters: { start: "{", end: "}" }
  });

  var secoes = (data.secoes || []).map(function(s) {
    return {
      titulo_secao: s.titulo_secao || s.titulo || "",
      conteudo_secao: s.conteudo_secao || (s.bullets ? s.bullets.join("\n") : "")
    };
  });

  // FIX 2: Add encerramento as last section
  if (data.encerramento && data.encerramento.trim()) {
    secoes.push({
      titulo_secao: "PALAVRA ABERTA E ENCERRAMENTO",
      conteudo_secao: "- " + data.encerramento.trim()
    });
  }

  console.log("[generateSumula] Rendering " + secoes.length + " secoes");

  doc.render({
    data: formatDate(data.data) || "",
    horario_inicio: data.horario_inicio || "",
    horario_fim: data.horario_fim || "",
    local: data.local || "",
    objetivo: data.objetivo || "",
    numero_reuniao: data.numero_reuniao || "",
    titulo_reuniao: data.titulo_reuniao || "",
    ano: data.ano || String(new Date().getFullYear()),
    informes: data.informes || "",
    pauta: data.pauta || [],
    secoes: secoes,
    quorum: data.quorum || [],
    anexos: data.anexos || []
  });

  var outputZip = doc.getZip();
  var docXml = outputZip.file("word/document.xml").asText();

  // Debug: check FIX00004 before postProcess
  var beforeCount = (docXml.match(/FIX00004/g) || []).length;
  console.log("[generateSumula] FIX00004 count before postProcess: " + beforeCount);

  // FIX 1: Replace content paragraphs with proper bullets
  docXml = postProcess(docXml, secoes);

  // Debug: check FIX00004 after postProcess
  var afterCount = (docXml.match(/FIX00004/g) || []).length;
  console.log("[generateSumula] FIX00004 count after postProcess: " + afterCount);
  console.log("[generateSumula] numPr count: " + (docXml.match(/w:numPr/g) || []).length);

  // FIX 3: Remove empty ANEXO
  if (!data.anexos || data.anexos.length === 0) {
    docXml = removeEmptyAnexo(docXml);
  }

  outputZip.file("word/document.xml", docXml);
  console.log("[generateSumula] Done!");
  return outputZip.generate({ type: "nodebuffer", compression: "DEFLATE" });
}

module.exports = { generateSumula: generateSumula };
